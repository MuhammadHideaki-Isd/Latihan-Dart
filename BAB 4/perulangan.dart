void main (){
  for (int i = 1; i <= 5; i++) {
    print("Udah diulang sebanyak $i kali");
  }
  print('----');

  // Contoh while
  int i = 1;
  while (i <= 5) {
    print('While ke-$i');
    i++;
  }

  print('----');

  // Contoh do-while
  int j = 1;
  do {
    print('Do-while ke-$j');
    j++;
  } while (j <= 5);
}


